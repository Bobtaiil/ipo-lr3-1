n = int(input("Введите число")) #запрос на ввод целого числа
if n%2 == 0: #конструкция if else; если остаток от деления на 2 равен 0, то число - чётное, иначе - нечётное
    print('Число чётное') #вывод при выполнении соотв. условия
else: #конструкция if else; если остаток от деления на 2 равен 0, то число - чётное, иначе - нечётное
    print('Число нечётное') #вывод при выполнении соотв. условия